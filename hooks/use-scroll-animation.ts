// Custom hook for scroll animations
export const useScrollAnimation = (
  threshold = 0.1,
  triggerOnce = true,
  rootMargin = "0px"
) => {
  const [ref, inView] = useInView({
    threshold,
    triggerOnce,
    rootMargin,
  });

  return {
    ref,
    inView,
    controls: inView ? "visible" : "hidden",
  };
};
